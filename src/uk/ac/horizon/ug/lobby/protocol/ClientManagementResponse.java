package uk.ac.horizon.ug.lobby.protocol;

/**
 * @author cmg
 *
 */
public class ClientManagementResponse {
	/** version */
	private int version;
	/** client id */
	private String clientId;
	/** status */
	private ClientManagementResponseStatus status;
	/** message */
	private String message;
	/** cons */
	public ClientManagementResponse() {}
	/**
	 * @return the version
	 */
	public int getVersion() {
		return version;
	}
	/**
	 * @param version the version to set
	 */
	public void setVersion(int version) {
		this.version = version;
	}
	/**
	 * @return the clientId
	 */
	public String getClientId() {
		return clientId;
	}
	/**
	 * @param clientId the clientId to set
	 */
	public void setClientId(String clientId) {
		this.clientId = clientId;
	}
	/**
	 * @return the status
	 */
	public ClientManagementResponseStatus getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(ClientManagementResponseStatus status) {
		this.status = status;
	}
	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}
	/**
	 * @param message the message to set
	 */
	public void setMessage(String message) {
		this.message = message;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "ClientManagementResponse [clientId=" + clientId + ", message="
				+ message + ", status=" + status + ", version=" + version + "]";
	}
	
}
